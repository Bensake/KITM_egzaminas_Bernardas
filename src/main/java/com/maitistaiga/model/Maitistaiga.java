package com.maitistaiga.model;

public class Maitistaiga {
    private int id;
    private String name;

    public Maitistaiga(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and setters
    public int getId() { return id; }
    public String getName() { return name; }
}